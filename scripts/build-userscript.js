const fs = require("node:fs");
const path = require("node:path");

const ROOT = path.resolve(__dirname, "..");
const SOURCE_DIR = path.join(ROOT, "src-userscript");
const OUT_PATH = path.join(ROOT, "userscripts", "prowlarr_seen_filter.user.js");
const MODULES = [
  "00-bootstrap.js",
  "10-api-hooks.js",
  "20-release-dedupe.js",
  "30-row-matching.js",
  "40-prowlarr-filters.js",
  "50-selection.js",
  "60-replay-actions.js",
  "70-toolbar.js",
  "80-main.js",
];

function readSource(name) {
  return fs.readFileSync(path.join(SOURCE_DIR, name), "utf8").replace(/\s*$/u, "");
}

const metadata = readSource("metadata.user.js");
const body = MODULES.map(readSource).join("\n\n");
const output =
  metadata +
  "\n\n// Generated from src-userscript/. Do not edit this file directly.\n" +
  "(function () {\n" +
  body +
  "\n})();\n";

fs.mkdirSync(path.dirname(OUT_PATH), { recursive: true });
fs.writeFileSync(OUT_PATH, output, "utf8");
console.log(`Built ${path.relative(ROOT, OUT_PATH)} from ${MODULES.length} modules.`);
